package com.classwork;

public class ForEachloop {

	public static void main(String[] args) {
		
		String name[]={"karan","Irfan","Amol","Shubham","Krishna"};
		
		//for-each loop
		//for each loop iterates in forward direction 
		for(String s:name)	
		{
			System.out.println(s);
		}

	}

}
